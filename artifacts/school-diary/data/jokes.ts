export function getWeightedScore(): number {
  const weights: Record<number, number> = {
    1: 5, 2: 8, 3: 12, 4: 15, 5: 20,
    6: 25, 7: 30, 8: 35, 9: 40, 10: 50,
  };
  const pool: number[] = [];
  for (const [score, w] of Object.entries(weights)) {
    for (let i = 0; i < w; i++) pool.push(Number(score));
  }
  return pool[Math.floor(Math.random() * pool.length)];
}

export function getJoke(score: number): string {
  const jokes = SCORE_JOKES[score] ?? [];
  return jokes[Math.floor(Math.random() * jokes.length)] ?? "";
}
